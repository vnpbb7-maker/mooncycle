/**
 * 節入り日テーブルを使って「命式上の月番号」を返す。
 * 節入り日前 → 前月扱い。テーブル範囲外の年は通常月をそのまま返す。
 * 節入り当日生まれの場合、出生時刻 < 節入り時刻なら前月扱いにする。
 */
function getMonthFromSekki(date: Date, birthHour: number = -1): number {
  const year  = date.getFullYear()
  const month = date.getMonth() + 1  // 1-12
  const day   = date.getDate()
  const dateKey = `${year}-${String(month).padStart(2,'0')}-${String(day).padStart(2,'0')}`

  // その年月の節入り日を検索
  let sekkiDay: number | null = null
  let sekkiKey: string | null = null
  for (const key of Object.keys(SEKKI_TABLE)) {
    const [y, m, d] = key.split('-').map(Number)
    if (y === year && m === month) { sekkiDay = d; sekkiKey = key; break }
  }

  // テーブル範囲外 → 通常月番号をそのまま返す
  if (sekkiDay === null) return month

  // 節入り当日: 出生時刻と節入り時刻を比較
  if (day === sekkiDay) {
    const sekkiHour = SEKKI_HOUR_JST[dateKey] ?? -1
    // 出生時刻が節入り時刻より前なら前月扱い
    if (birthHour >= 0 && sekkiHour >= 0 && birthHour < sekkiHour) {
      const prevMonth = month === 1 ? 12 : month - 1
      const prevYear  = month === 1 ? year - 1 : year
      for (const key of Object.keys(SEKKI_TABLE)) {
        const [y, m] = key.split('-').map(Number)
        if (y === prevYear && m === prevMonth) return SEKKI_TABLE[key]
      }
      return prevMonth
    }
    // 節入り時刻以降なら当月
    return SEKKI_TABLE[sekkiKey!]
  }

  // 節入り日「より前」の場合は前月扱い
  if (day < sekkiDay) {
    const prevMonth = month === 1 ? 12 : month - 1
    const prevYear  = month === 1 ? year - 1 : year
    for (const key of Object.keys(SEKKI_TABLE)) {
      const [y, m] = key.split('-').map(Number)
      if (y === prevYear && m === prevMonth) return SEKKI_TABLE[key]
    }
    return prevMonth
  }

  // 節入り日以降 → 当月の節入り月番号
  return SEKKI_TABLE[sekkiKey!]
}

